      HAL_ADC_Stop(&hadc3);


      counterAdc++;

      if(counterAdc>100)
      {
    	  counterAdc=0;
      }
	  sprintf(adcbuffer0,"\n\r %lu \n\r",adcraw0);
	  HAL_UART_Transmit(&huart3, (uint8_t*)adcbuffer0, strlen(adcbuffer0), HAL_MAX_DELAY);
